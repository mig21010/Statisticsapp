                        	<p>Los pronósticos o predicciones son una herramienta esencial en cualquier proceso de toma de decisiones. La calidad de los pronósticos que se realizan está estrechamente relacionada con la información que se puede extraer y utilizar a partir de los datos históricos. El análisis de series del tiempo es un método cuantitativo que utilizamos para determinar patrones en los datos recolectados a través del tiempo.</p>
                            <p>El análisis de series de tiempo se utiliza para detectar particiones, en cambios con la información estadística en intervalos regulares. Proyectamos estos patrones para obtener una estimación para el futuro. En consecuencia el análisis de series de tiempo nos ayuda a manejar la incertidumbre asociada con los acontecimientos futuros.</p>
                        <h4 class="card-title">Variación en las series de tiempo</h4>
                            <p>Utilizamos el término series para referirnos a cualquier grupo de información estadística que se acumula en intervalos regulares, existen cuatro tipos:</p>
                            <li><b>Tendencia Secular (Tiempo real):</b> El valor de la variable tiende a aumentar o disminuir en un periodo muy largo. Ejemplo: los precios al consumidor, donde estos varían bastante, pero en un periodo largo de tendencia tienden a aumentar de manera estable.</li><br>
                            <img src="../public/imagenes/tsecular.jpg" class="card-img-top img-responsive" alt="Card image cap"><br><br>
                            <li><b>Fluctuación cíclica:</b> La variable sube y baja sobre la línea de tendencia secular. Ejemplo: el ciclo económico el cual llega a pico pero en otras disminuye pero aun así se mantiene sobre la tendencia.</li><br>
                            <img src="../public/imagenes/cciclico.jpg" class="card-img-top img-responsive" alt="Card image cap"><br><br>
                            <li><b>Variación estacional:</b> Patrón de cambio en el lapso de un año que tiende a repetirse anualmente. Ejemplo: un medico aumenta sus pacientes en época de invierno por casos de gripe, y en verano aumento por tifoidea.</li><br>
                            <img src="../public/imagenes/cestacional.jpg" class="card-img-top img-responsive" alt="Card image cap"><br><br>
